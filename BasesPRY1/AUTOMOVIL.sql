CREATE TABLE [dbo].[AUTOMOVIL]
(
	[MODELO] VARCHAR(20) NOT NULL, 
    [ANO] NCHAR(4) NOT NULL, 
    CHECK (LEN(ANO) = 4),
    [DETALLE] VARCHAR(50) NOT NULL,
    [NOMBRE_FAB] VARCHAR(50) NOT NULL, 
    FOREIGN KEY (NOMBRE_FAB) REFERENCES FAB_AUTOS (NOMBRE)
        ON DELETE NO ACTION
        ON UPDATE CASCADE,
    PRIMARY KEY (MODELO, ANO)
)
