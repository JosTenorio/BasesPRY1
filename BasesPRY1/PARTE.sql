CREATE TABLE [dbo].[PARTE]
(
	[ID] INT NOT NULL PRIMARY KEY IDENTITY, 
    [NOMBRE] VARCHAR(50) NOT NULL, 
    [FABRICANTE] INT NOT NULL,
    [MARCA] INT NOT NULL,
    [MODELO_AUTO] VARCHAR(20) NULL, 
    [ANO_AUTO] NCHAR(4) NULL, 
    FOREIGN KEY ([FABRICANTE]) REFERENCES FAB_PARTES (ID)
        ON DELETE NO ACTION
        ON UPDATE CASCADE,
    FOREIGN KEY (MARCA) REFERENCES MARCA (ID)
        ON DELETE NO ACTION
        ON UPDATE CASCADE,
    FOREIGN KEY (MODELO_AUTO, ANO_AUTO) REFERENCES AUTOMOVIL (MODELO, ANO)
        ON DELETE NO ACTION
        ON UPDATE CASCADE,
)
