CREATE TABLE [dbo].[TELEFONOS_ORGANIZACION]
(
	[CEDULA_ORGANIZACION] NCHAR(10) NOT NULL, 
    [TELEFONO] NCHAR(8) NOT NULL, 
	CONSTRAINT CH_TELORG CHECK (LEN(TELEFONO) = 8),
	CONSTRAINT FK_TELORG_ORG FOREIGN KEY ([CEDULA_ORGANIZACION]) REFERENCES ORGANIZACION ([CEDULA_JUR])
	    ON DELETE CASCADE 
        ON UPDATE CASCADE,
	CONSTRAINT PK_TELORG PRIMARY KEY ([CEDULA_ORGANIZACION], TELEFONO)
)
