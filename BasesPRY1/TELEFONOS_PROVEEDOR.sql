CREATE TABLE [dbo].[TELEFONOS_PROVEEDOR]
(
	[ID_PROVEEDOR] INT NOT NULL , 
    [TELEFONO] NCHAR(8) NOT NULL, 
	CONSTRAINT CH_TELPROV CHECK (LEN(TELEFONO) = 8),
	CONSTRAINT FK_TELPROV_PROV FOREIGN KEY ([ID_PROVEEDOR]) REFERENCES PROVEEDOR (ID)
	    ON DELETE NO ACTION
        ON UPDATE CASCADE,
	CONSTRAINT PK_TELPROV PRIMARY KEY ([ID_PROVEEDOR], TELEFONO)
)
