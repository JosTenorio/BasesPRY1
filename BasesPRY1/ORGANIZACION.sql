CREATE TABLE [dbo].[ORGANIZACION]
(
    [CEDULA_JUR] NCHAR(10) NOT NULL,
    CONSTRAINT CH1_CEDJ CHECK (LEN(CEDULA_JUR) = 10),
    CONSTRAINT CH2_CEDJ CHECK (CEDULA_JUR NOT LIKE '%[^0-9]%'),
    [NOMBRE] NVARCHAR(50) NOT NULL, 
    [DIRECCION] NVARCHAR(70) NOT NULL, 
    [CIUDAD] NVARCHAR(20) NOT NULL, 
    [NOMBRE_CONTACTO] NVARCHAR(50) NOT NULL,
    [CARGO_CONTACTO] NVARCHAR(50) NOT NULL,
    [ID_CLIENTE] INT NOT NULL, 
    CONSTRAINT FK_ORG_CL FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE (ID)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT PK_ORG PRIMARY KEY CLUSTERED ([CEDULA_JUR] ASC)
)
