CREATE TABLE ORDEN
(
	[ID] INT NOT NULL PRIMARY KEY IDENTITY, 
    [MONTO_BASE] DECIMAL(18, 2) NOT NULL DEFAULT 0, 
    CHECK ([MONTO_BASE] >= 0),
    [FECHA] DATE NOT NULL DEFAULT GETDATE(), 
    [ID_CLIENTE] INT NOT NULL, 
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE (ID)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
)
