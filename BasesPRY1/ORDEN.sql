CREATE TABLE ORDEN
(
	[ID] INT NOT NULL IDENTITY, 
    [MONTO_BASE] DECIMAL(18, 2) NOT NULL DEFAULT 0, 
    CONSTRAINT CH_MONTBASE CHECK ([MONTO_BASE] >= 0),
    [FECHA] DATE NOT NULL DEFAULT GETDATE(), 
    [ID_CLIENTE] INT NOT NULL, 
    CONSTRAINT FK_ORD_CL FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE (ID)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT PK_ORDEN PRIMARY KEY (ID)
)
